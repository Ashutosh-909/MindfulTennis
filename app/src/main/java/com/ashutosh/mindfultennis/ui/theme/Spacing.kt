package com.ashutosh.mindfultennis.ui.theme

import androidx.compose.ui.unit.dp

/**
 * Spacing tokens for consistent layout across the app.
 */
object Spacing {
    /** 4 dp — Inline icon gaps */
    val xs = 4.dp

    /** 8 dp — Between related elements */
    val sm = 8.dp

    /** 16 dp — Content padding, card internal padding */
    val md = 16.dp

    /** 24 dp — Between sections */
    val lg = 24.dp

    /** 32 dp — Screen edge top/bottom margins */
    val xl = 32.dp
}
